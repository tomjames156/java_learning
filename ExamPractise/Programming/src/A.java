public class A{
    int i;
    int j;

    public A(int a, int b){
        i = a;
        j = b;
    }

    /**
     * desc: a function that prints out i/x and j/y coordinates of A
     * @param NULL
     * @return void
     */

    public void show(){
        System.out.println("Point A (x, y) = " + "(" + i + ", " + j + ")");
    }
}